/**
 * _abs - computes the absolute value of an integer
 * @n: the integer to compute the absolute value of
 *
 * Return: the absolute value of n
 */
int _abs(int n)
	/* using the conditional ternary operator to write the cod*/
{
	return (n >= 0 ? n : -n);
}
